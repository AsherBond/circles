namespace OCA\Circles\Service;


use OCP\IL10N;


/**
 * Class ExceptionService
 *
 * @package OCA\Circles\Service
 */
class ExceptionService {


	/** @var IL10N */
	private $l10n;


	/**
	 * ExceptionService constructor.
	 *
	 * @param IL10N $l10n
	 */
	public function __construct(IL10N $l10n) {
		$this->l10n = $l10n;
	}


	/**
	 * @return IL10N
	 */
	public function getL10n(): IL10N {
		return $this->l10n;
	}

}

